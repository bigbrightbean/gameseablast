function loadImage(base){

const formats = [".png",".jpg",".jpeg"]
let i = 0

function tryNext(){
  if(i >= formats.length){
    imageEl.src = "zimages/zplaceholder.png"
    return
  }

  const img = new Image()
  img.src = base + formats[i]

  img.onload = ()=> imageEl.src = img.src

  img.onerror = ()=>{
    i++
    tryNext()
  }
}

tryNext()

}


